#include <REGX51.H>
	ORG 0000H
	AJMP STA
	ORG 0030H
STA:MOV TMOD,#10H
	MOV TH1,#0FFH
	MOV TL1,#0FFH
	SETB TR1

AG:	CLR A
	MOV P3,#0FFH
	ACALL DEL
	CLR A
	MOV P3,#00H
	ACALL DEL
	CLR A
	AJMP AG

	
DEL:JNB TF1,DEL
	CLR TF1
	INC A
	CJNE A,#24H,DEL
	CLR TF1
	SETB TR1
	RET
	END
