#include <REGX51.H>
		ORG 0000H
		AJMP START
		ORG 0030H
START:	MOV TL1,#0FFH
		MOV TH1,#0FFH
		MOV TMOD,#10H
		SETB TR1
AGAIN:	MOV P3,#0FFH
		CLR A
		ACALL DELAY
		CLR A
		MOV P3,#00H
		ACALL DELAY
		CLR A
		AJMP AGAIN
DELAY:	JNB TF1,DELAY
		INC A
		CLR TF1
		CJNE A,#23H,DELAY
		CLR TF1
		SETB TR1
		RET
		END